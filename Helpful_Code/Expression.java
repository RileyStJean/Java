package Java.Helpful_Code;


public class Expression {

    public static void main(String args[]){

        //Expression = Operands & Operations
        //Operands = Value, Variables, Number and Quantity
        //Operations - +, -, /, %


        //Creating Variables
        //Int Variables
        int add = 20;
        int minus = 40;
        int divide = 60;
        int multipli = 3;
        int group = 30;

        //Double Variables
        double doubleDivide = 10;

        //Putting Operations in operands also know as a Expression
        add = add + 10;
        minus = minus - 10;
        divide = divide / 2;
        multipli = multipli * 10;
        group = group % 11;
        doubleDivide = doubleDivide / 3;

        //Printing output
        System.out.println("Add :" + add);
        System.out.println("Minus : " + minus);
        System.out.println("Divide : " + divide);
        System.out.println("multiplication : " + multipli);
        System.out.println("Grouped : "+ group);
        System.out.println("Double : "+ doubleDivide);


    }



}










